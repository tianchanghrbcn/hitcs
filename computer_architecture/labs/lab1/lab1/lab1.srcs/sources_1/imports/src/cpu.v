`timescale 1ns / 1ps

`include "define.v"

module cpu(
    input            clk,
    input            resetn,

    output           inst_sram_en,       
    output [31:0]    inst_sram_addr,     
    input  [31:0]    inst_sram_rdata,    

    output           data_sram_en,       
    output [3:0]     data_sram_wen,       
    output [31:0]    data_sram_addr,     
    output [31:0]    data_sram_wdata,    
    input  [31:0]    data_sram_rdata,    

    output [31:0]    debug_wb_pc     ,  
    output           debug_wb_rf_wen ,  
    output [4 :0]    debug_wb_rf_wnum,  
    output [31:0]    debug_wb_rf_wdata   
    );

    wire [31:0] IF_PC;
    wire [31:0] next_PC;
    wire [31:0] IF_NPC;
    wire [31:0] IF_IR;
    
    //ID
    wire [31:0] ID_PC;
    wire [31:0] ID_NPC;
    wire [31:0] ID_A;
    wire [31:0] ID_B;
    wire [31:0] ID_Imm;
    wire [31:0] ID_IR;
    
    //EX
    wire [31:0] EX_PC;
    wire [31:0] EX_NPC;
    wire [31:0] EX_A;
    wire [31:0] EX_B;
    wire [31:0] EX_Imm;
    wire [31:0] EX_IR;
    wire [4:0]  EX_shamt;
    
    wire        EX_Cond;
    wire [31:0] EX_ALU_output;
    wire        EX_wb_en;
    
    wire [31:0] alu_data_1;
    wire [31:0] alu_data_2;
    
    //MEM
    wire [31:0] MEM_PC;
    wire [31:0] MEM_ALU_output;
    wire [31:0] MEM_B;
    wire [31:0] MEM_IR;
    wire [31:0] MEM_LMD;
    wire        MEM_wb_en;
    
    //WB
    wire [31:0] WB_PC;
    wire [31:0] WB_LMD;
    wire [31:0] WB_ALU_output;
    wire [31:0] WB_IR;
    wire        WB_wb_en;
    
    wire reg_write_enable;
    wire [4:0]  WB_address;
    wire [31:0] WB_data;
   
    assign debug_wb_pc = WB_PC;
    assign debug_wb_rf_wen = WB_wb_en;
    assign debug_wb_rf_wnum = WB_address;
    assign debug_wb_rf_wdata = WB_data;
    
    wire stall;
// ?? stall ????????????
assign stall = (
    // ??1?ID ??? rs ??? (ID_IR[25:21]) ?? 0??? EX ???????? (EX_IR[20:16] ? EX_IR[15:11]) ??
    // ?? ID ??????? EX ???????????? EX ???????
    ((ID_IR[25:21] != 0) && ((ID_IR[25:21] == EX_IR[20:16]) || (ID_IR[25:21] == EX_IR[15:11]))) ||
    
    // ??2?ID ??? rs ??? (ID_IR[25:21]) ?? 0??? MEM ???????? (MEM_IR[20:16] ? MEM_IR[15:11]) ??
    // ?? ID ??????? MEM ???????????? MEM ?????
    ((ID_IR[25:21] != 0) && ((ID_IR[25:21] == MEM_IR[20:16]) || (ID_IR[25:21] == MEM_IR[15:11]))) ||
    
    // ??3?ID ??? rs ??? (ID_IR[25:21]) ?? 0??? WB ???????? (WB_IR[20:16] ? WB_IR[15:11]) ??
    // ?? ID ??????? WB ???????????? WB ?????
    ((ID_IR[25:21] != 0) && ((ID_IR[25:21] == WB_IR[20:16]) || (ID_IR[25:21] == WB_IR[15:11]))) ||
    
    // ??4?ID ??? rt ??? (ID_IR[20:16]) ?? 0??? EX ???????? (EX_IR[20:16] ? EX_IR[15:11]) ??
    // ?? ID ??????? EX ???????????? EX ???????
    ((ID_IR[20:16] != 0) && ((ID_IR[20:16] == EX_IR[20:16]) || (ID_IR[20:16] == EX_IR[15:11]))) ||
    
    // ??5?ID ??? rt ??? (ID_IR[20:16]) ?? 0??? MEM ???????? (MEM_IR[20:16] ? MEM_IR[15:11]) ??
    // ?? ID ??????? MEM ???????????? MEM ?????
    ((ID_IR[20:16] != 0) && ((ID_IR[20:16] == MEM_IR[20:16]) || (ID_IR[20:16] == MEM_IR[15:11]))) ||
    
    // ??6?ID ??? rt ??? (ID_IR[20:16]) ?? 0??? WB ???????? (WB_IR[20:16] ? WB_IR[15:11]) ??
    // ?? ID ??????? WB ???????????? WB ?????
    ((ID_IR[20:16] != 0) && ((ID_IR[20:16] == WB_IR[20:16]) || (ID_IR[20:16] == WB_IR[15:11])))
);


    
    //IF 
    PC pc(
        .clk            (clk),
        .reset_n        (resetn),
        .new_address    (IF_NPC),
        .stall          (stall),
        
        .output_address (IF_PC)
    );
    
    ADD PC_plus4(
        .data1          (IF_PC), 
        .data2          (32'h00000004), 
        .result         (next_PC)
    );

    assign inst_sram_en = 1'b1;
    assign inst_sram_addr = IF_PC;
    assign IF_IR = inst_sram_rdata;
    
    reg [31:0] IF_PC_reg;
    reg [31:0] IF_IR_reg;
    reg stall_d1; 
    
    always @(posedge clk) begin
        if (!resetn) begin
            IF_PC_reg <= 32'b0;
        end else if (!stall) begin
            IF_PC_reg <= IF_PC;
        end
    end
    
// ??????????
always @(posedge clk) begin
    // ? resetn ????????????? IF_IR_reg ? stall_d1
    if (!resetn) begin
        IF_IR_reg <= 32'b0;      // IF ??????????
        stall_d1 <= 1'b0;        // ????? stall ????
    end else begin
        // ??????? stall ??? stall_d1???????
        stall_d1 <= stall;

        // ??1?????????? (stall) ?????????? (stall_d1 ??)
        // ???????????????? IF ????? IF_IR_reg??????
        if (stall && !stall_d1) begin
            IF_IR_reg <= IF_IR;  // ???????????? IF ????
            stall_d1 <= 1'b1;    // ???????
        end 
        // ??2??????????????
        // ?? IF_IR_reg ??????????????
        else if (stall) begin
            IF_IR_reg <= IF_IR_reg;  // ?? IF_IR_reg ???????
        end 
        // ??3?????????????????
        // ??????????? IF_IR_reg ??
        else if (!stall && stall_d1) begin
            IF_IR_reg <= IF_IR_reg;  // ???????????????
            stall_d1 <= 1'b0;        // ??????
        end 
        // ??4???????????
        // ??? IF ??????? IF_IR_reg
        else begin
            IF_IR_reg <= IF_IR;      // ?? IF_IR_reg ???? IF ????
        end
    end
end

// ????
reg [1:0] forwardA, forwardB;  // ??????? ALU ????????
wire EX_MEM_RegWrite;          // EX/MEM ?????????
wire MEM_WB_RegWrite;          // MEM/WB ?????????
wire [4:0] EX_MEM_RegisterRd;  // EX/MEM ?????????
wire [4:0] MEM_WB_RegisterRd;  // MEM/WB ?????????
wire [4:0] ID_EX_RegisterRs;   // ID/EX ??????? rs
wire [4:0] ID_EX_RegisterRt;   // ID/EX ??????? rt

// ???????
parameter FORWARD_FROM_EX_MEM = 2'b10;  // ? EX/MEM ?????
parameter FORWARD_FROM_MEM_WB = 2'b01;  // ? MEM/WB ?????
parameter NO_FORWARD          = 2'b00;  // ????????????????

// ???????????
always @(*) begin
    // ?????????
    forwardA = 2'b00;  // ALU??????????00????????01????EX???10????MEM??
    forwardB = 2'b00;  // ALU??????????00????????01????EX???10????MEM??

    // EX??????????????????EX?????
    if ((EX_MEM_RegWrite) && (EX_MEM_RegisterRd != 0) && (EX_MEM_RegisterRd == ID_EX_RegisterRs)) begin
        forwardA = 2'b10;  // ??MEM??????????ALU???????
    end

    if ((EX_MEM_RegWrite) && (EX_MEM_RegisterRd != 0) && (EX_MEM_RegisterRd == ID_EX_RegisterRt)) begin
        forwardB = 2'b10;  // ??MEM??????????ALU???????
    end

    // ???EX???????????????EX??
    if ((MEM_WB_RegWrite) && (MEM_WB_RegisterRd != 0) && (MEM_WB_RegisterRd == ID_EX_RegisterRs)) begin
        forwardA = 2'b01;  // ??EX??????????ALU???????
    end

    if ((MEM_WB_RegWrite) && (MEM_WB_RegisterRd != 0) && (MEM_WB_RegisterRd == ID_EX_RegisterRt)) begin
        forwardB = 2'b01;  // ??EX??????????ALU???????
    end
end

    
    MUX PC_mux(
        .data0          (MEM_ALU_output),
        .data1          (next_PC),
        .select         (EX_Cond),
        .result         (IF_NPC)
    );
    
    // ??? IF_ID_registers ??????? IF ????? ID ??? NPC?IR ? PC
IF_ID_registers IF_ID_reg(
    .clk            (clk),         // ????
    .reset_n        (resetn),      // ??????????

    // ????? IF ??? NPC?IR ? PC
    .NPC_i          (IF_NPC),      // ?????????NPC?Next PC?
    .IR_i           (stall_d1 ? IF_IR_reg : IF_IR), // ????????????????????? IF_IR_reg??????? IF ?????
    .PC_i           (IF_PC_reg),   // ??????

    // ???????
    .stall          (stall),       // ?????????????????
    
    // ?????? ID ??? NPC?IR ? PC
    .NPC_o          (ID_NPC),      // ??? ID ??? NPC
    .IR_o           (ID_IR),       // ??? ID ??????IR?
    .PC_o           (ID_PC)        // ??? ID ??????????PC?
);


    
    //ID
    Extender extender(
        .opcode         (ID_IR[31:26]),
        .input_26bImm   (ID_IR[25:0]),
        .output_32bImm  (ID_Imm)
    );
    
    Registers Registers_set(
        .clk            (clk),
        .reset_n        (resetn),
        .write_enable   (WB_wb_en),
        .rs             (ID_IR[25:21]),
        .rt             (ID_IR[20:16]),
        .writeback_address(WB_address),
        .writeback_data (WB_data),
        .output_data_A  (ID_A),
        .output_data_B  (ID_B)
    );
    

    // ??? ID_EX_registers ??????? ID ????? EX ??? NPC?A?B?Imm32?IR ? PC ???
ID_EX_registers ID_EX_reg(
    .clk            (clk),        // ????
    .reset_n        (resetn),     // ??????????
    
    // ????? ID ??? NPC?A?B?Imm32?IR ? PC
    .NPC_i          (ID_NPC),     // ? ID ????? NPC??????????
    .A_i            (ID_A),       // ? ID ???????? A ??
    .B_i            (ID_B),       // ? ID ???????? B ??
    .Imm32_i        (ID_Imm),     // ? ID ????????????
    .IR_i           (ID_IR),      // ? ID ????????IR?
    .PC_i           (ID_PC),      // ? ID ????????????PC?
    .shamt_i        (ID_IR[10:6]), // ????????shamt??? IR ? [10:6] ???
    
    // ???????
    .stall          (stall),      // ?????????????????
    
    // ?????? EX ??? NPC?A?B?Imm32?IR ? PC
    .NPC_o          (EX_NPC),     // ??? EX ??? NPC
    .A_o            (EX_A),       // ??? EX ?????? A ??
    .B_o            (EX_B),       // ??? EX ?????? B ??
    .Imm32_o        (EX_Imm),     // ??? EX ??????????
    .IR_o           (EX_IR),      // ??? EX ??????IR?
    .PC_o           (EX_PC),      // ??? EX ??????????PC?
    .shamt_o        (EX_shamt)    // ??? EX ???????shamt?
);


    
    //EX
    MUX1 mux1(
        .opcode         (EX_IR[31:26]),
        .data0          (EX_NPC),
        .data1          (EX_A),
        .result         (alu_data_1)
    );
    
    MUX2 mux2(
        .opcode         (EX_IR[31:26]),
        .data0          (EX_B),
        .data1          (EX_Imm),
        .result         (alu_data_2)
    );
    
    Equal equal(
        .opcode         (IF_IR[31:26]),
        .data_A         (EX_A),
        .data_B         (EX_B),
        .equal          (EX_Cond)
    );
    
    ALU alu(
        .opcode         (EX_IR[31:26]),
        .alu_op         (EX_IR[5:0]),
        .input_data1    (alu_data_1),
        .input_data2    (alu_data_2),
        .shamt          (EX_shamt),
        .IR             (EX_IR),
        
        .result         (EX_ALU_output),
        .wb_en          (EX_wb_en)
    );
    
    assign data_sram_en = 1'b1;
    assign data_sram_wdata = EX_B;
    assign data_sram_addr = EX_ALU_output;
    assign data_sram_wen = (EX_IR[31:26] == `OP_SW) ? 4'b1111 : 4'b0000; // SW 指令
    
    EX_MEM_registers EX_MEM_reg(
        .clk            (clk),
        .reset_n        (resetn), 
        .ALU_output_i   (EX_ALU_output),
        .B_i            (EX_B),
        .IR_i           (EX_IR),
        .PC_i           (EX_PC),
        .wb_en_i        (EX_wb_en),
        
        .ALU_output_o   (MEM_ALU_output),
        .B_o            (MEM_B),
        .IR_o           (MEM_IR),
        .PC_o           (MEM_PC),
        .wb_en_o        (MEM_wb_en)
    );
    
    assign MEM_LMD = data_sram_rdata;

    MEM_WB_registers MEM_WB_reg(
        .clk            (clk),
        .reset_n        (resetn),
        .LMD_i          (MEM_LMD),
        .ALU_output_i   (MEM_ALU_output),  
        .IR_i           (MEM_IR),
        .PC_i           (MEM_PC),
        .wb_en_i        (MEM_wb_en),

        
        .LMD_o          (WB_LMD),
        .ALU_output_o   (WB_ALU_output),
        .IR_o           (WB_IR),
        .PC_o           (WB_PC),
        .wb_en_o        (WB_wb_en)
);
    
    //WB
    Writeback_aMUX Writeback_aMUX_inst(
        .opcode         (WB_IR[31:26]),
        .rt_address     (WB_IR[20:16]),
        .rd_address     (WB_IR[15:11]),
        .writeback_address(WB_address)
    );
    
    Writeback_dMUX Writeback_dMUX_inst(
        .opcode         (WB_IR[31:26]),
        .data0          (WB_LMD),
        .data1          (WB_ALU_output),
        .writeback_data (WB_data)
    );
    
endmodule